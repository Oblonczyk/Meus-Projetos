""" 
Python = Linguagem de Programação
Tipo de Tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Vinycius Oblonczyk')

# Aspas duplas
print("Vinycius Oblonczyk")

# Escape (CONTRA BARRA PULA O PRÓX CARACTERE)
print("Vinycius \"Oblonczyk\"")
#No terminal ira aparecer as aspas, só que serão executadas como caracteres

#r (MOSTRARÁ O CARACTERE DE BARRA)
print(r"Vinycius \"Oblonczyk\"")

# A forma mais simples de inserir aspas dentro de strings:
print('Vinycius "Oblonczyk"')
print("Vinycius 'Oblonczyk'")